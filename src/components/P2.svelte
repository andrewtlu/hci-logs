      <p class="pheading">POV Statements</p>
      <ul class="plist">
        <li>
          People managing long-term dietary conditions need clear and reliable
          food transparency because uncertainty increases medical risk and
          mental burden.
        </li>
        <li>
          Individuals who depend on insulin need accurate and accessible
          macronutrient information because insulin timing and dosage require
          precise carbohydrate estimation.
        </li>
        <li>
          Students and professionals with strict dietary needs prefer
          predictable food options because spontaneity disrupts health routines
          and glucose control.
        </li>
        <li>
          People living with chronic dietary conditions need to feel included
          without being singled out because social belonging is as important as
          physical safety.
        </li>
        <li>
          Individuals managing dietary conditions need to trust food
          environments because past mislabeling, unclear labeling, and guessing
          reduce confidence.
        </li>
        <li>
          Long-term condition managers need to reduce cognitive load because
          constant monitoring, estimation, and anticipation are mentally
          exhausting.
        </li>
      </ul>

      <p class="pheading">HMW Statements</p>
      <ul class="plist">
        <li>
          How might we provide clear and reliable ingredient and macronutrient
          transparency across different dining environments?
        </li>
        <li>
          How might we reduce the cognitive load required to evaluate food
          choices throughout the day?
        </li>
        <li>
          How might we support insulin-dependent users in anticipating glucose
          impact before eating?
        </li>
        <li>
          How might we improve trust in institutional dining systems and food
          labeling?
        </li>
        <li>
          How might we provide clarity in non-standardized food settings such as
          potlucks, conferences, or social gatherings?
        </li>
        <li>
          How might we make food information accessible in real time, including
          availability and timing?
        </li>
        <li>
          How might we normalize dietary differences so users feel included
          rather than singled out?
        </li>
        <li>
          How might we support balanced moderation instead of rigid restriction?
        </li>
        <li>
          How might we help users maintain safe spontaneity in social dining
          situations?
        </li>
        <li>
          How might we design for multiple dietary conditions within one
          inclusive framework?
        </li>
        <li>
          How might we increase users’ confidence when making food decisions
          outside their home environment?
        </li>
      </ul>

      <p class="pheading">Identified Needs</p>
      <ul class="plist">
        <li>
          Need accurate and reliable information about ingredients and
          macronutrients.
        </li>
        <li>
          Need to anticipate how food will affect glucose levels or symptoms
          before eating.
        </li>
        <li>
          Need predictable and consistent meal timing to maintain health
          balance.
        </li>
        <li>Need to reduce constant mental calculations and guesswork.</li>
        <li>
          Need confidence when making food decisions in uncertain environments.
        </li>
        <li>
          Need clear information in non-standardized settings such as potlucks,
          conferences, or social gatherings.
        </li>
        <li>Need real-time awareness of what food is available.</li>
        <li>Need to feel included without being singled out.</li>
        <li>Need autonomy in food choices while maintaining safety.</li>
      </ul>

      <p class="pheading">Conclusion</p>
      <p class="pbody">
        The interviews revealed that individuals managing long-term dietary
        conditions face ongoing cognitive, medical and social demands that
        extend beyond simple food restrictions. Although many participants
        describe their conditions as manageable, their daily lives involve
        constant anticipation, calculation, and risk assessment, especially in
        environments where ingredient transparency and food availability are
        unclear. So, the findings highlight the importance of improving clarity
        and trust with the food environment for people managing dietary
        conditions.
      </p>
